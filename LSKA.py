class FFN(nn.Module):
    def __init__(self,dim):
        super().__init__()
        self.bn = nn.BatchNorm2d(dim)
        # self.conv0h = nn.Conv2d(dim, dim, kernel_size=(1, 3), stride=(1, 1), padding=(0, (3 - 1) // 2), groups=dim)
        # self.conv0v = nn.Conv2d(dim, dim, kernel_size=(3, 1), stride=(1, 1), padding=((3 - 1) // 2, 0), groups=dim)
        self.conv_d = nn.Conv2d(dim,dim,kernel_size=3,stride=1,padding=1,groups=dim)
        self.gelu = nn.GELU()
        self.conv1 = nn.Conv2d(dim,dim,kernel_size=1)

    def forward(self,x):
        x = self.conv1(x)
        # x = self.conv0h(x)
        # x = self.conv0v(x)
        x = self.conv_d(x)
        x = self.gelu(x)
        x = self.conv1(x)

        return x


class LSKA(nn.Module):
    def __init__(self,c1,c2,kernel):
        super().__init__()
        self.bn = nn.BatchNorm2d(c1)
        self.conv1 = nn.Conv2d(c1,c2,kernel_size=1)
        self.gelu = nn.GELU()
        self.lska = Attention(c1,kernel)
        self.ffn = FFN(c1)

    def forward(self,x):
        
        shortcut = x.clone()
        x = self.bn(x)
        x = self.conv1(x)
        x = self.gelu(x)
        x = self.lska(x)
        x = self.conv1(x)
        x = x + shortcut
        shortcut = x.clone()
        x = self.bn(x)
        x = self.ffn(x)
        x = x + shortcut
        
        return x

class Bottleneck(nn.Module):
    """Standard bottleneck."""

    def __init__(self, c1, c2, shortcut=True, g=1, k=(3, 3), e=0.5):
        """Initializes a standard bottleneck module with optional shortcut connection and configurable parameters."""
        super().__init__()
        c_ = int(c2 * e)  # hidden channels
        self.cv1 = Conv(c1, c_, k[0], 1)
        self.cv2 = Conv(c_, c2, k[1], 1, g=g)
        self.add = shortcut and c1 == c2

    def forward(self, x):
        """Applies the YOLO FPN to input data."""
        return x + self.cv2(self.cv1(x)) if self.add else self.cv2(self.cv1(x))

class ALKA(nn.Module):
    """Faster Implementation of CSP Bottleneck with 2 convolutions."""

    def __init__(self, c1, c2, n=1, kernel=23, Default=True, replace=0, g=1, e=0.5):
        """Initializes a CSP bottleneck with 2 convolutions and n Bottleneck blocks for faster processing."""
        super().__init__()
        if Default == False:
            n = replace

        # print(n)
        self.c = int(c2 * e)  # hidden channels
        self.cv1 = Conv(c1, 2 * self.c, 1, 1)
        self.cv2 = Conv((2 + n) * self.c, c2, 1)  # optional act=FReLU(c2)
        if kernel == 3:
            shortcut = True
            self.m = nn.ModuleList(Bottleneck(self.c, self.c, shortcut, g, k=((3, 3), (3, 3)), e=1.0) for _ in range(n))
        else:
            self.m = nn.ModuleList(LSKA(self.c, self.c, kernel) for _ in range(n))

    def forward(self, x):
        """Forward pass through C2f layer."""
        y = list(self.cv1(x).chunk(2, 1))
        y.extend(m(y[-1]) for m in self.m)
        return self.cv2(torch.cat(y, 1))

    def forward_split(self, x):
        """Forward pass using split() instead of chunk()."""
        y = list(self.cv1(x).split((self.c, self.c), 1))
        y.extend(m(y[-1]) for m in self.m)
        return self.cv2(torch.cat(y, 1))